package org.odpi.openmetadata.frameworks.openmetadata.properties.assets.databases;

import com.fasterxml.jackson.annotation.JsonAutoDetect;
import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonInclude;
import org.odpi.openmetadata.frameworks.openmetadata.properties.assets.DataSetProperties;
import org.odpi.openmetadata.frameworks.openmetadata.types.OpenMetadataType;

import static com.fasterxml.jackson.annotation.JsonAutoDetect.Visibility.NONE;
import static com.fasterxml.jackson.annotation.JsonAutoDetect.Visibility.PUBLIC_ONLY;

/**
 * TableDataSetProperties is a class for representing a database table that is significant enough to be an asset.
 */
@JsonAutoDetect(getterVisibility=PUBLIC_ONLY, setterVisibility=PUBLIC_ONLY, fieldVisibility=NONE)
@JsonInclude(JsonInclude.Include.NON_NULL)
@JsonIgnoreProperties(ignoreUnknown=true)
public class TableDataSetProperties extends DataSetProperties
{
    /**
     * Default constructor
     */
    public TableDataSetProperties()
    {
        super();
        super.setTypeName(OpenMetadataType.TABLE_DATA_SET.typeName);
    }


    /**
     * Copy/clone constructor.
     *
     * @param template object to copy
     */
    public TableDataSetProperties(TableDataSetProperties template)
    {
        super(template);
    }


    /**
     * Standard toString method.
     *
     * @return print out of variables in a JSON-style
     */
    @Override
    public String toString()
    {
        return "TableDataSetProperties{} " + super.toString();
    }
}
